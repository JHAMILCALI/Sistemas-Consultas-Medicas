    private void botonListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonListarActionPerformed
        // TODO add your handling code here:
        NodoConsulta R = v1.getA().getConsulta().getP();
        String fechaX = campoFecha.getText();
        String titulos[] = {"\t====== PACIENTES DE LA FECHA "+fechaX+" ======"};
        DefaultTableModel pantalla = new DefaultTableModel(null,titulos);
        boolean sw = false;
        if (!fechaX.equals("")) {
            while (R!=null) {
			Consulta consul = R.getConsulta();
			if (consul.getFecha().equals(fechaX)) {
                                sw = true;
				String nombrePaciente = consul.getPaciente().getNombre();
                                String pacientes [] = {"Paciente: "+nombrePaciente};
                                pantalla.addRow(pacientes);
			}
			R=R.getSig();
		}
        if (!sw) {
            JOptionPane.showMessageDialog(null,"No se encontro Pacientes de la fecha "+fechaX);
        }
        }else{
            JOptionPane.showMessageDialog(null,"Datos no validos");
        }
		
        pantallaFechas.setModel(pantalla);
    }//GEN-LAST:event_botonListarActionPerformed

